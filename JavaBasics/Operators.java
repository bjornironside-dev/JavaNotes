package JavaLanguage.JavaBasics;

public class Operators {
    public static void main(String[] args) {
        int x = 10;
        int y = 2;
        int z;

        //Arithmetic operator
        z = x + y;  //Addition
        z = x - y;  //Subtraction
        z = x * y;  //Multiplication
        z = x / y;  //Division
        z = x%y;    //Modulus Operator(gives remainder)


        //Augmented Assignment operator
        x+=y;   // x = x + y
        x-=y;   // x = x - y
        x*=y;   // x = x * y
        x/=y;   // x = x/y

        //Increment and decrement operators
        x++;    //x = x + 1
        x--;    //x = x - 1

        //ORDER OF OPERATION (P-E-M-D-A-S)

    }
}
